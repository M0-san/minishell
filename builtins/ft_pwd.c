{
	char	pwd[1025];

	// getcwd(pwd, 1024);
	// printf("%s\n", pwd);
	int r;
	while ((r = read(0, pwd, 1024)) > 0)
	{
		pwd[r] = '\0';
		printf("%s", pwd);
	}
	return (0);
}
